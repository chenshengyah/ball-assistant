import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const projectDirectory = path.resolve(scriptDirectory, "..");
const outputPath = path.join(
  projectDirectory,
  "miniprogram",
  "config",
  "private.ts"
);

function readEnv(name, fallback = "") {
  const value = process.env[name];

  return typeof value === "string" && value.trim() ? value.trim() : fallback;
}

function serializeEnvironment(runtimeEnvironment) {
  return `export const miniappPrivateEnv = ${JSON.stringify(
    runtimeEnvironment,
    null,
    2
  )} as const;\n`;
}

function normalizeRuntimeEnvironment(value) {
  return value === "local" ? "local" : "production";
}

const currentEnv = normalizeRuntimeEnvironment(
  readEnv("MINIAPP_RUNTIME_ENV", "production")
);
const localApiBaseUrl = readEnv(
  "MINIAPP_LOCAL_API_BASE_URL",
  "http://127.0.0.1:3000/api"
);
const productionApiBaseUrl = readEnv(
  "MINIAPP_PRODUCTION_API_BASE_URL",
  readEnv(
    "MINIAPP_TEST_API_BASE_URL",
    readEnv("MINIAPP_API_BASE_URL", "https://ball-assistant.cloud/api")
  )
);
const apiBaseUrl =
  currentEnv === "local" ? localApiBaseUrl : productionApiBaseUrl;

await fs.mkdir(path.dirname(outputPath), { recursive: true });
await fs.writeFile(
  outputPath,
  [
    "// This file is generated by apps/miniapp/scripts/generate-miniapp-env.mjs.",
    "// Do not commit secrets here. The file is gitignored on purpose.",
    serializeEnvironment({
      currentEnv,
      apiBaseUrl,
      environments: {
        local: {
          label: "Local API",
          apiBaseUrl: localApiBaseUrl,
        },
        production: {
          label: "Production API",
          apiBaseUrl: productionApiBaseUrl,
        },
      },
    }),
  ].join("\n")
);

console.log(`Generated miniapp runtime config at ${outputPath}`);
